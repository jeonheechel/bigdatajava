package bean;

public class StarDTO {
	private String id;
	private int num;
	private int top;
	private int under;
	private int shoes;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getNum() {
		return num;
	}
	public void setNum(int num) {
		this.num = num;
	}
	public int getTop() {
		return top;
	}
	public void setTop(int top) {
		this.top = top;
	}
	public int getUnder() {
		return under;
	}
	public void setUnder(int under) {
		this.under = under;
	}
	public int getShoes() {
		return shoes;
	}
	public void setShoes(int shoes) {
		this.shoes = shoes;
	}
	
	
	
	
	
	
	
	
	
	
	

}
