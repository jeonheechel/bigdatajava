package 반복문;

import java.util.Scanner;

public class ForQuiz5 {
	
	public static void main(String[] args) {
		int i;
		int j;
		for( i=2; i<10; i++) {
			System.out.println(i+"단");
			
		for( j=1; j<10; j++) {
			System.out.print(" "+i +"*"+ j +" = "+  i*j);
		}
		System.out.println();
		}
		System.out.println();
		
		
		
		
		
		
		
	}
	
}
